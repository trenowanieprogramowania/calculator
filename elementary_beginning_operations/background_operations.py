def addition(first_number, second_number) -> float:
    return float(first_number) + float(second_number)


def subtraction(first_argument, second_argument) -> float:
    return float(first_argument) - float(second_argument)


def multiplication(first_input, second_input) -> float:
    return float(first_input) * float(second_input)


def division(input1, input2) -> float:
    return float(input1) / float(input2)


def nullification(argument1, argument2) -> float:
    return 0.0


def terminating_programme(data1, data2):
    exit()
